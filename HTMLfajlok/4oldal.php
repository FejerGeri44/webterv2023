<p class="text1">A Szovjetunió, Németország bátorítása mellett, <span>1940 júniusában</span> elfoglalta a balti államokat. Ugyanezen hónap 10. napján a tengelyhatalmakhoz <em>(Németország szövetségeseihez)</em> tartozó <span>Olaszország is belépett a háborúba</span>. A nácik 1940. július 10-étől 31-ei vereségükig légi háborút vívtak Anglia fölött, amely angliai csata néven ismertté vált.
</p><br>

<p class="text1">Miután a németek 1941. április 6-án Jugoszlávia és Görögország megszállásával bebiztosították a balkáni régiót, <span>június 22-én</span>, a német-szovjet egyezményt nyíltan megsértve, szövetségeseikkel együtt megtámadták a Szovjetuniót. Adolf Hitler ezen döntése történészek véleméyne szerint az legelső súlyos hiba volt.
</p><br>

