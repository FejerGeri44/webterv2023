<p class="text">A német haderő <span>1940. tavaszán</span> betört Nyugat-Európába. Franciaország megtamása sikerrel járt, a német birodalom kiterjesztette határát az óceánra is, valamint a francia ellenállás letörése után létrehoztak egy szimpatizáns francia államot az ország déli részén, a <span>Vichy-kormányt.</span>
</p><br>

