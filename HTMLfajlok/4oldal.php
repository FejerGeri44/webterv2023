<p class="text1">Június és július folyamán <span>(a térség melegebb időszakában)</span> a németek a balti államokat is elfoglalták. <span>Joszif Sztálin</span> szovjet vezető ezutána náci Németország és a tengelyhatalmak ellen fellépő szövetségesek egyik vezéralakjává vált a háborúban. 1941 nyarán és őszén a német csapatok mélyen benyomultak a szovjet területekre, december 6-án pedig a szovjet erők lendültek jelentős ellentámadásba.
</p>

<p class="text1"><span>1942 májusában</span> a Brit Királyi Légierő légicsapást mért a németországi Köln városára ezer bombázóval; ekkor tört be a háború először Németország területére. A következő három év alatt a szövetséges légierők szisztematikusan bombázták a Birodalom ipari létesítményeit és nagyvárosait, 1945-re porig rombolták a német városok jó részét.
</p><br>

