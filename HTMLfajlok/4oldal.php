<p class="text1">Nyár végére a német offenzíva mindkét fronton <span>elakadt.</span> Novemberben a szovjet csapatok ellentámadásba kezdtek Sztálingrádnál, és<span> 1943. február 2-án</span> a 6. német hadsereg megadta magát. A németek újabb offenzívát indítottak 1943 júliusában Kurszk mellett, melyet a történelem legnagyobb tankösszecsapásaként tartanak számon.
</p><br>

